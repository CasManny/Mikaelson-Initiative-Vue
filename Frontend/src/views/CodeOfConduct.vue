              <h2>5. Professional and Academic Integrity</h2>
              
              <h3>5.1 Academic Honesty</h3>
              <p>All members must maintain the highest standards of academic and professional integrity:</p>
              <ul>
                <li>Avoiding plagiarism and properly citing all sources</li>
                <li>Submitting original work and collaborating ethically</li>
                <li>Respecting intellectual property rights</li>
                <li>Being honest about qualifications, achievements, and experience</li>
                <li>Maintaining confidentiality of sensitive information</li>
              </ul>
              
              <h3>5.2 Professional Representation</h3>
              <p>When representing the Mikaelson Innovation and Community Development Initiative (Mikaelson Initiative) or participating in community activities:</p>
              <ul>
                <li>Conduct yourself in a manner that reflects positively on the community</li>
                <li>Respect organizational policies and procedures</li>
                <li>Maintain professional boundaries in all relationships</li>
                <li>Avoid conflicts of interest and disclose potential conflicts</li>
                <li>Protect the reputation and integrity of the organization</li>
              </ul>
            </section>

