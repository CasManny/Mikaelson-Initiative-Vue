              <h3>2.1 Expected Behavior</h3>
              <p>Examples of behavior that contributes to a positive environment for our community include:</p>
              <ul>
                <li>Demonstrating empathy and kindness toward other people</li>
                <li>Being respectful of differing opinions, viewpoints, and experiences</li>
                <li>Giving and gracefully accepting constructive feedback</li>
                <li>Accepting responsibility and apologizing to those affected by our mistakes</li>
                <li>Focusing on what is best not just for us as individuals, but for the overall community</li>
                <li>Using welcoming and inclusive language</li>
                <li>Supporting fellow members in their personal and professional growth</li>
                <li>Sharing knowledge, resources, and opportunities generously</li>
                <li>Celebrating diversity and promoting equal participation</li>
              </ul>
              
              <h3>2.2 Unacceptable Behavior</h3>
              <p>Examples of unacceptable behavior include:</p>
              <ul>
                <li>The use of sexualized language or imagery, and sexual attention or advances of any kind</li>
                <li>Trolling, insulting or derogatory comments, and personal or political attacks</li>
                <li>Public or private harassment, intimidation, or stalking</li>
                <li>Publishing others' private information, such as a physical or email address, without their explicit permission</li>
                <li>Discrimination or prejudice based on any protected characteristic</li>
                <li>Disruptive behavior during events, meetings, or online discussions</li>
                <li>Deliberate spreading of misinformation or false claims</li>
                <li>Inappropriate use of nudity and/or sexual images in community spaces</li>
                <li>Other conduct which could reasonably be considered inappropriate in a professional setting</li>
              </ul>
