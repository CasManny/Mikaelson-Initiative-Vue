              <h2>4. Participation Guidelines</h2>
              
              <h3>4.1 Event and Program Conduct</h3>
              <p>During Mikaelson Innovation and Community Development Initiative (Mikaelson Initiative) events, workshops, programs, and activities:</p>
              <ul>
                <li>Arrive on time and come prepared with necessary materials</li>
                <li>Participate actively and constructively in all activities</li>
                <li>Respect speakers, facilitators, organizers, and fellow participants</li>
                <li>Follow health and safety guidelines and protocols</li>
                <li>Keep mobile devices on silent during sessions unless otherwise instructed</li>
                <li>Respect venue rules, regulations, and property</li>
                <li>Use designated channels for feedback, questions, and concerns</li>
                <li>Dress appropriately for the occasion and setting</li>
              </ul>
              
              <h3>4.2 Online Community Standards</h3>
              <p>In our digital spaces, including social media groups, forums, video calls, and virtual events:</p>
              <ul>
                <li>Keep discussions relevant, constructive, and on-topic</li>
                <li>Avoid spam, excessive self-promotion, or irrelevant content</li>
                <li>Respect privacy, confidentiality, and intellectual property</li>
                <li>Give proper credit and attribution when sharing others' work</li>
                <li>Use appropriate language, tone, and professional communication</li>
                <li>Report violations promptly to moderators or administrators</li>
                <li>Follow platform-specific guidelines and terms of service</li>
                <li>Maintain appropriate backgrounds and attire during video calls</li>
              </ul>
            </section>

